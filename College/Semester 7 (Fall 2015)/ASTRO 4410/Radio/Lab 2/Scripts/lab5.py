def fminf(frequency, amplitude, p):
    fminindex=0
    for i in amplitude:
        if i < p:
            fminindex=fminindex+1
        else:
            break
    return frequency[fminindex]

def fmaxf(frequency, amplitude, p):
    fmaxindex=len(frequency)
    reverse=amplitude[::-1]
    for i in reverse:
        if i < p:
            fmaxindex=fmaxindex-1
        else:
            break
    return frequency[fmaxindex]

